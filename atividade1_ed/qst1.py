# 1. Crie um programa que deverá imprimir todos os números inteiros de 20 até 100.

from time import sleep
cont = 20

print("Contagem de 20 a 100:")
while cont < 101:
    sleep(0.15)
    print(cont)
    cont+=1
print("PROGRAMA FINALIZADO...")